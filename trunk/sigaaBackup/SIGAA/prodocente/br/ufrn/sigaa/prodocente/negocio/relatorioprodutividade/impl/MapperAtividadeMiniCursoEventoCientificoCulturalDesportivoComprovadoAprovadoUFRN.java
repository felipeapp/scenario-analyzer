package br.ufrn.sigaa.prodocente.negocio.relatorioprodutividade.impl;

import java.util.Collection;

import br.ufrn.arq.erros.DAOException;
import br.ufrn.sigaa.arq.dao.prodocente.AvaliacaoDocenteDao;
import br.ufrn.sigaa.pessoa.dominio.Servidor;
import br.ufrn.sigaa.prodocente.negocio.relatorioprodutividade.AbstractMapperAtividade;
import br.ufrn.sigaa.prodocente.relatorios.dominio.ViewAtividadeBuilder;

/**
 * prodocente.item_relatorio_produtividade.id_item_relatorio_produtividade = 54
 * Descrição = Mini-cursos em eventos científicos, culturais e desportivos,  comprovados por certificados e aprovados em instância competentes na UFRN 
 * item 4.7
 * @author Victor Hugo
 *
 */
public class MapperAtividadeMiniCursoEventoCientificoCulturalDesportivoComprovadoAprovadoUFRN
		extends AbstractMapperAtividade {

	@Override
	public Collection<? extends ViewAtividadeBuilder> buscarAtividades(
			Servidor docente, Integer ano, Integer validade) throws DAOException {
		
		AvaliacaoDocenteDao dao = null;
		try{
			dao = new AvaliacaoDocenteDao();
			return dao.findRelatorioMiniCursoEvento(docente, ano, validade);
		}finally{ dao.close(); }
		
	}
	
}